    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    // Obtener la fila seleccionada
    int selectedRow = jTable1.getSelectedRow();

    // Verificar si hay una fila seleccionada
    if (selectedRow != -1) {
        // Obtener el valor del PID de la columna correspondiente (columna 0 en este caso)
        String pid = (String) jTable1.getValueAt(selectedRow, 0);

        // Crear una instancia del controlador
        Controlador controlador = new Controlador();

        // Llamar al m√©todo del controlador para matar el proceso con el PID seleccionado
        boolean killed = controlador.killProcess(pid);

        // Notificar si el proceso fue terminado exitosamente
        if (killed) {
            javax.swing.JOptionPane.showMessageDialog(this, "Proceso con PID " + pid + " terminado.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al terminar el proceso con PID " + pid + ".");
        }
    } else {
        // Si no hay una fila seleccionada, mostramos un mensaje
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor selecciona un proceso en la tabla.");
    }
}

